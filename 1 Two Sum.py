class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        n = len(nums)
        for i in range(n):
            for j in range(i+1, n):
                if nums[i] + nums[j] == target:
                    return [i, j]
                    
class Solution(object): #trading space for speed
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        hashTable = {}
        n = len(nums)
        for i, num in enumerate(nums):
            try:
                return [hashTable[num], i]
            except KeyError:
                hashTable[target-num] = i            